class calculator{
    static xarisxi (a, b) {
        return Math.pow(a, b);
    }
    static add(a, b){
        return a + b;
    };
    static minus(a, b){
        return a - b;
    };
    static multiply (a, b){
        return a * b;
    };
    static divide (a, b){
        return a / b;
    };
}