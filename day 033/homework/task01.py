# Inline CSS შეუძლია ძალიან ადვილად, ერთი ბრძანებით დეზაინი თვითონ HTML ფაილში, მაგალითად: <p style="color: blue;"ეს ტექსტი იქნება ლურჯი</p>

# Internal CSS ასევე არის HTML ფაილში და <head> ბრძანების შორის ისმება <style>

# External CSS სჭირდება ცალკე ფაილი და უმეტესად არქმევენ "styles.css". აქ ყველანაირი დეზაინი შეგიძლია გააკეთო დეტალურად